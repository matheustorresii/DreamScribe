import SwiftUI

extension Color {
    static var dreamPurple: Self {
        return .init(.init(red: 40/255, green: 34/255, blue: 62/255, alpha: 1.0))
    }
    
    static var dreamPink: Self {
        return .init(.init(red: 251/255, green: 243/255, blue: 255/255, alpha: 1.0))
    }
}
